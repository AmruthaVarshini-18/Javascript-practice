//Arthimetic Operators
let a = 10;
let b = 8;
//1.Addition(+)
console.log("a+b=", a + b);
//2.Subtraction(-)
console.log("a-b=", a - b);
//3.Multiplication(*)
console.log("a*b=", a * b);
//4.Division(/)
console.log("a/b=", a / b);
//5.Modulus(%)
console.log("a%b=", a % b);
//6.Exponentation(**)
console.log("a**b=", a ** b);

//Unary Operators
//1.Increement(++)
//pre increement
console.log("a=", a);
console.log("++a=", ++a);
console.log("a=", a);
//post increement
console.log("a=", a);
console.log("a++=", a++);
console.log("a=", a);
//2.Decreement
//predecreement
console.log("a=", a);
console.log("--a=", --a);
console.log("a=", a);
//postdecreement
console.log("a=", a);
console.log("a--=", a--);
console.log("a=", a);
