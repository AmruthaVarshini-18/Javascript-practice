//Logical Operators
let x = 25;
let y = 23;
let cond1 = x != y;
let cond2 = x >= y;
console.log("x=", x);
console.log("y=", y);
console.log("cond1=", cond1);
console.log("cond2=", cond2);
//Logical AND(&&)
console.log("cond1&&cond2=", cond1 && cond2);
//Logical OR(||)
console.log("cond1||cond2=", cond1 || cond2);
//Logical NOT(!)
console.log("!cond1", !cond1);
