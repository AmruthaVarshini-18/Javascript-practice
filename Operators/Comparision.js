//Comparision Operators
//Equal to(==)
let a = 5;
let b = "5";
console.log("a=", a);
console.log("b=", b);
console.log("a==b", a == b);
//Notequal(!=)
console.log("a!=b", a != b);
//Equalto&type(===)
console.log("a===b", a === b);
//NotEqualto&type(!==)
console.log("a!==b", a !== b);

let x = 10;
let y = 20;
console.log("x=", x);
console.log("y=", y);
//<
console.log("x<y", x < y);
//>
console.log("x>y", x < y);
//<=
console.log("x<=y", x < y);
//>=
console.log("x>=y", x < y);
