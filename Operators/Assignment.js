//Assignment Operators
//=
let a = 4;
let b = 2;
//+=
console.log("a+=1", (a += 1));
console.log("a+=b", (a += b));
console.log("a=", a);
//-=
console.log("a-=1", (a -= 1));
console.log("a-=b", (a -= b));
console.log("a=", a);
//*=
console.log("a*=1", (a *= 1));
console.log("a*=b", (a *= b));
console.log("a=", a);
//%=
console.log("a%=1", (a %= 1));
console.log("a%=b", (a %= b));
console.log("a=", a);
//**=
console.log("a**=3", (a **= 3));
console.log("a**=b", (a **= b));
console.log("a=", a);
